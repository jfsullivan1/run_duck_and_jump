			gc.pushMatrix();
			for (int i=1; i<theta1.length; i++)
			{
				gc.rotate(theta1[i]);
				
	
				gc.stroke(LINK_COLOR);
				gc.strokeWeight(LINK_THICKNESS);
				gc.line(0, 0, L1[i], 0);	
				gc.translate(L1[i], 0);
			}
