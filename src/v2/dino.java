				gc.rotate(theta4[i]);
				
	
				gc.stroke(LINK_COLOR);
				gc.strokeWeight(LINK_THICKNESS);
				gc.line(0, 0, L4[i], 0);
		
	
				gc.translate(L4[i], 0);
