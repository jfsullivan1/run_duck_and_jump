				rect(10, 45, 765, 65);
				rect(210, 460, 350, 60);
				rect(210, 620, 340, 60);
				textSize(titleSize);
				fill(148, 0, 211);
				text(splashtxt, 30, 100);
				textSize(txtSize);
				text(splashtxtClick, 220, 500);
